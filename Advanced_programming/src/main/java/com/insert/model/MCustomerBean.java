package com.insert.model;



public class MCustomerBean {
    private String cus_name;
    private String cus_nic;
    private String cus_address;
    private String cus_hometown;
    private String cus_tele;
    private String cus_email;

    public String getCus_name() {
        return cus_name;
    }
    public void setCus_name(String cus_name) {
        this.cus_name = cus_name;
    }

    public String getCus_nic() {
        return cus_nic;
    }
    public void setCus_nic(String cus_nic) {
        this.cus_nic = cus_nic;
    }

    public String getCus_address() {
        return cus_address;
    }
    public void setCus_address(String cus_address) {
        this.cus_address = cus_address;
    }

    public String getCus_hometown() {
        return cus_hometown;
    }
    public void setCus_hometown(String cus_hometown) {
        this.cus_hometown = cus_hometown;
    }

    public String getCus_tele() {
        return cus_tele;
    }
    public void setCus_tele(String cus_tele) {
        this.cus_tele = cus_tele;
    }

    public String getCus_email() {
        return cus_email;
    }
    public void setCus_email(String cus_email) {
        this.cus_email = cus_email;
    }
}
