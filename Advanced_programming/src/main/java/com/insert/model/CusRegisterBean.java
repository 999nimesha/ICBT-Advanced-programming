package com.insert.model;

public class CusRegisterBean {
	
	private String customerName;
	private String customerNic;
	private String customerAddress;
	private String customerHometown;
	private String customerTele;
	private String customerEmail;
	
	public String getCustomer_Name() {
		return customerName;
	}
	public void setCustomer_Name(String customerName) {
		this.customerName = customerName;
	}
	public String getCustomer_Nic() {
		return customerNic;
	}
	public void setCustomer_Nic(String customerNic) {
		this.customerNic = customerNic;
	}
	public String getCustomer_Address() {
		return customerAddress;
	}
	public void setCustomer_Address(String customerAddress) {
		this.customerAddress = customerAddress;
	}
	public String getCustomer_Hometown() {
		return customerHometown;
	}
	public void setCustomer_Hometown(String customerHometown) {
		this.customerHometown = customerHometown;
	}
	public String getCustomer_Tele() {
		return customerTele;
	}
	public void setCustomer_Tele(String customerTele) {
		this.customerTele = customerTele;
	}
	public String getCustomer_Email() {
		return customerEmail;
	}
	public void setCustomer_Email(String customerEmail) {
		this.customerEmail = customerEmail;
	}
	
	//getters and setters
	
}

